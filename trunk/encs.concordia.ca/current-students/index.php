  <h2>Newly admitted students</h2>
  <p>ENCS Student Academic Services has put together a special advising guide for all incoming students. Find out more about the <a href="newly-admitted-students/index.html">guide for new students</a>.</p>
  <h2>Undergraduate program requirements</h2>
  <p>Find all of the <a href="undergraduate-program-requirements/index.html">requirements for undergraduate programs</a>, such as course sequences, information on the writing test, capstone projects, grades and graduation evaluations.</p>
  <h2>Graduate program requirements</h2>
  <p>Find all of the <a href="http://www.encs.concordia.ca/current-students/graduate-program-requirements/">requirements for graduate programs</a>, including the graduate student handbook.</p>
  <h2>Registration</h2>
  <p>Wondering how to add or drop a course or what the appropriate credit load for your course is? Check out our <a href="http://www.encs.concordia.ca/current-students/registration/">Registration pages</a>.</p>
  <h2>Forms &amp; procedures</h2>
  <p>Download the student request form, find out about the expectation of originality. Find all the <a href="http://www.encs.concordia.ca/current-students/forms-and-procedures/">academic forms and procedures</a>.</p>
  <h2>International exchanges</h2>
  <p>Would you like to study in another country and have your courses credited at Concordia? The <a href="http://www.encs.concordia.ca/current-students/international-exchanges/">International Student Exchange Program</a> offers a unique opportunity for both undergraduate and graduate students to study overseas.</p>
  <h2>Industrial experience</h2>
  <p>To provide more practical experience, non-Co-op undergraduate students will have the option of registering for a work term during the summer semester in the <a href="http://www.encs.concordia.ca/current-students/industrial-experience/">industrial experience program</a>.</p>
