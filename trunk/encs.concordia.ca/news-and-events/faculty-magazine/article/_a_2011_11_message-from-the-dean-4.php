Concordia Engineering News! With so much exciting progress being made in this
dynamic field, we thought that this was the perfect time to share some of our
aerospace-related stories with you.</p>

<p class="MsoNormal">For a decade now, the Concordia Institute for Aerospace
Design and Innovation has led the way in aerospace teaching and research in
Montreal. As CIADI celebrates its tenth anniversary, we take a look back on how
it all got started and provide a forecast as to where it's headed. From the
Institute's beginnings under the leadership of Hany Moustapha to its current
incarnation with the dynamism and expertise of Bob Fews at its helm, CIADI has
come a long way with its mandate of providing leading edge training for
engineering students in aerospace. </p>

<p class="MsoNormal">Speaking of students, just like the Faculty of Engineering
and Computer Science, CIADI would be nothing without them! To that end, we're
happy to introduce you to two perfect examples of what it means to be a CIADI
intern. In the pages that follow, you'll meet Anthony Toteda, an undergraduate
whose CIADI studies have taken him all the way to Europe; and Master's student
Michelle Boyce, whose research has taken her from Antarctica to Outer Space!</p>

<p class="MsoNormal">With our interdepartmental aerospace faculty consolidated under
one umbrella on the 12<sup>th</sup> floor of the EV Building, aerospace
research is also taking off. Just take Luis Rodrigues, for example. The
Associate Professor from the Department of Electrical and Computer Engineering
is pioneering new research into autopilots. Whereas Suong van Hoa from the
Department of Mechanical and Industrial Engineering is doing innovative work on
automated manufacturing of lightweight composites-- both professors are
generating significant breakthroughs that will make commercial aircraft
greener. </p>

<p class="MsoNormal">It's no surprise that our alumni are also intimately
involved in the aerospace industry. Despite the fact that Sami Girgis graduated
before CIADI even existed, the internship he undertook at Pratt and Whitney
Canada while still an undergrad at Concordia led him directly into a career in
aerospace. And we've got four future alums featured in our "Co-Op Corner," who
just might follow in Girgis's footsteps!</p>

<p class="MsoNormal">From undergraduates to PhD candidates, professors to alumni,
this is a Faculty that is truly focused on aerospace. I hope that you'll enjoy
learning more about our involvement in this ever-evolving industry and look
forward to sharing more good news on the subject very soon....</p>

<p class="MsoNormal">Until then, happy reading and best wishes for a productive
autumn,</p>

<p class="MsoNormal">&nbsp;</p>

<p class="MsoNormal" style="margin-bottom:0in;margin-bottom:.0001pt">Robin A. L.
Drew, </p>

<p class="MsoNormal" style="margin-bottom:0in;margin-bottom:.0001pt">Dean of the
