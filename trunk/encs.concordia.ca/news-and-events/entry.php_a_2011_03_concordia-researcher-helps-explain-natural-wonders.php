		<div class="entry-content">
			<div class="entry-body">
				
Research coming out of Concordia often impacts our day-to-day lives. Groundbreaking insights into everything from bullying to obesity, domestic violence to power tools, have given us a better understanding of how the world around us works. Lately, the findings of one Concordia researcher have been reaching for the sky...and beyond! <br /><br /><div align="center"><img alt="Vatistas-hurricane-saturn-web.jpg" src="http://newsfeed.encs.concordia.ca/encs_test/Vatistas-hurricane-saturn-web.jpg" class="mt-image-center" style="text-align: center; display: block; margin: 0pt auto 20px;" height="291" width="440" /><font style="font-size: 0.8em;"><i>The research of George Vatistas has been used to predict hurricanes and explain the hexagonal shape of the jet-stream that flows over Saturn's north pole<br /></i></font><br /></div>In 2008, <a href="http://www.me.concordia.ca/people/faculty/vatistas">George Vatistas</a>, Professor in the <a href="http://www.me.concordia.ca/">Department of Mechanical and Industrial Engineering</a>, within Concordia's Faculty of Engineering and Computer Science, <a href="http://cjournal.concordia.ca/archives/20080417/researchers_confirm_124yearold_theorem.php">made headlines</a> when he definitively proved that Nobel Prize winner J. J. Thomson's theory on the stability of ring satellite vortices was pragmatically sound. For decades prior to that, Vatistas had been making waves with his research into vortices. Now, that work is being taken up by scientists from south of the border to help explain natural mysteries.<br /><br />In the latest edition of the <i><a href="http://journals.ametsoc.org/toc/atsc/0/0">Journal of the Atmospheric Sciences</a></i> (the renowned peer-reviewed journal of the American Meteorological Society), meteorologist Vincent Wood (from The National Oceanic and Atmospheric Administration-Severe Storms Laboratory) along with Luther White (from University of Oklahoma-Department of Mathematics) cite Vatistas's research over 20 times in their article, "A New Parametric Model of Vortex Tangential-Wind Profiles: Development, Testing and Verification."<br /><br />The article looks uses what they call the "Vatistas Model" to help predict hurricanes and typhoons--natural disasters that cause substantial losses in life and property worldwide every year. Co-authors Wood and White extend Vatistas's basic vortex model with the aim to characterize realistic-looking wind profiles encountered in dust devils, waterspouts, tornadoes, meso-cyclones and tropical cyclones. Through this application of Vatistas's research, meteorologists will be able to more accurately predict the intensity of these catastrophic vortices and shed light on the scientific reasons for their destructive nature.<br /><br />Meanwhile, the research on satellite vortices has been recently used as
an example of laboratory finding in support of a natural phenomenon
beyond the earthly realities of tornadoes.
: the unique hexagonal shape of the jet-stream that flows over Saturn's north pole. First observed by NASA's Voyager mission, the pattern has persisted for over thirty years. In an article entitled "Emergence of polar jet polygons from jet instabilities in a Saturn model," in the latest edition of <i><a href="http://www.elsevier.com/wps/find/journaldescription.cws_home/622843/description#description">Icarus</a></i>, a peer-reviewed journal devoted to the publication of original contributions in the field of Solar System studies, co-authors Raul Morales-Juberias, Kunio M. Syanagi, Timothy E. Dowling, and Andrew P. Ingersoil use Vatistas's boratory findings on vortex patterns to provide an
example of an earthly experimental evidence to this seemingly unnatural
shape.
<br /><br />With work being done by Concordia researchers having such far-reaching impact, it is no wonder that the Faculty of Engineering and Computer Science is quickly emerging as a leader in the field. Vatistas has already seen his work form the basis of a generalized approach to wake characterization in fixed wing aircraft, helicopters, ship propulsion, the aerodynamics of insect-like flapping wings, gas turbine engines, wind turbines, optics, acoustics, and many others. He expects that his research will continue to have far-reaching implications in the years to come and looks forward to sharing his results with his colleagues at Concordia and around the world.<br />

