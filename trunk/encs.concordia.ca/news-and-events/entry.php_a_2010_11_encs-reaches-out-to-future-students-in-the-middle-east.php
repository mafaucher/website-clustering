Faculty reaches out to future students in the Middle East
</h2>-->
			<div class="entry-meta" title="2010-11-19T10:01:50-05:00">Friday Nov 19, 2010</div>
		</div>
	
		<div class="entry-content">
			<div class="entry-body">
				
With a large percentage of students from Concordia's Faculty of Engineering and Computer Science hailing from the Middle East, recruitment outreach programs such as the <a href="http://www.tradecommissioner.gc.ca/eng/document.jsp?did=107588">Middle East Education Initiative</a> (MEEI) help the Faculty to maintain its ties to these countries that have come to see Concordia as go-to institution for tertiary education. Designed to facilitate meetings between Canadian representatives of educational institutions and potential students, the MEEI (which is organized by Foreign Affairs and International Trade Canada) is made up of recruitment-focused events that include fairs, school visits, networking receptions and market briefings.<br /><br /><div align="center"><img alt="MEEI-1.jpg" src="http://newsfeed.encs.concordia.ca/encs_test/MEEI-1.jpg" class="mt-image-center" style="text-align: center; display: block; margin: 0pt auto 20px;" height="330" width="440" /><i>Christopher Trueman and Dalia Radwan representing Concordia</i><br /></div><br />Concordia's Faculty of Engineering and Computer Science was one of 16 participating Canadian institutions, which included universities from across the country. The Faculty's representatives were: <a href="../administration-and-governance/associate-deans-and-special-advisor/academic-programs/index.html">Lata Narayanan</a>, Associate Dean of Academic Programs; <a href="../administration-and-governance/associate-deans-and-special-advisor/academic-affairs/index.html">Christopher Trueman</a>, Associate Dean of Academic Affairs; and Dalia Radwan, Advisor with <a href="../administration-and-governance/administrative-units/student-academic-services/">Student Academic Services</a>. <br /><br />The three were pleased to take part in the initiative, which helped the Faculty to maintain its already positive contacts with the academic counsellors in the schools that were visited in Jordan, Syria, Lebanon and Egypt. They focused on international schools within these countries, because the dual citizenship of the bulk of their students makes the transition to studying at Concordia that much easier. <br /><br /><div align="center"><img alt="MEEI-2.jpg" src="http://newsfeed.encs.concordia.ca/encs_test/MEEI-2.jpg" class="mt-image-center" style="text-align: center; display: block; margin: 0pt auto 20px;" height="370" width="440" /><i>Lata Narayanan and Dalia Radwan attend a reception held by the Ambassador 
of Canada in Damascus, Syria</i><br /></div><br />Radwan, whose role within the Faculty of Engineering and Computer Science sees her dealing directly with current and future students on a day-to-day basis, notes that she was "impressed to learn that the academic programs and cultural experiences offered at Concordia University are recognized and respected around the Middle East."&nbsp; She is looking forward to welcoming more academically driven and dynamic students from the region to the Faculty of Engineering and Computer Science.<br /><br />During the trip, Trueman noted that "many young engineers working in industry in Egypt would like to come to Concordia for a masters or doctoral program." Now back at the office after his time abroad, Trueman recalls being "astonished" during the Cairo education fair "at the large number of young people interested in coming to Montreal to study at Concordia, both for undergraduate students and for graduate programs." Not only did Trueman see great interest in Concordia's engineering courses, particularly in the fields of mechanical and electrical engineering, he also saw that there was tremendous interest in all the faculties: in the MBA program in the John Molson School of Business, in biology, chemistry, physics and psychology in Arts and Science. <br /><br /><div align="center"><img alt="MEEI-3.jpg" src="http://newsfeed.encs.concordia.ca/encs_test/MEEI-3.jpg" class="mt-image-center" style="text-align: center; display: block; margin: 0pt auto 20px;" height="330" width="440" /><i>Dalia Radwan and associates attend a reception </i><i>held by the Ambassador of 
Canada in Egypt (pictured centre) at his house in Cairo</i><br /></div><br />For Narayanan, it was "very interesting to talk to young, motivated, and articulate high school students who are planning to go abroad for their undergraduate studies." She also found it "encouraging to learn that Montreal and Concordia were very well-known in the schools we went to--many school counsellors told us that their students were especially interested in Concordia." <br /><br />Faculty officials say they intend to continue encourage this interest abroad by taking part in this government initiative in the years to come. They hope to expand their travels to include countries such as the UAE, Qatar, Saudi Arabia and Tunisia in their efforts to promote Concordia's Faculty of Engineering and Computer Science in the Middle East. <br />

