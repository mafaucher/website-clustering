	  <p>The <a href="../../administration-and-governance/administrative-units/academic-and-personnel-affairs/index.html">Academic and Personnel Affairs unit</a> focuses on the faculty personnel and teaching aspects of academic administration and manages full- and part-time personnel and teaching activities, as well as teaching assistants. It coordinates full- and part-time academic personnel and teaching activities in accordance with CUFA and CUPFA collective agreements and University policies. It is also responsible for the maintenance and development of the FIS to meet ongoing needs of the Faculty. </p>

