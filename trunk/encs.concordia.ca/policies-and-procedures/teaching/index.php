	<p>Full- and part-time instructors as well as teaching assistants with the Faculty of Engineering and Computer Science will find information on the following in this section:</p>
	<h2>Full-time faculty</h2>
	<p>Find out specific details on workloads for <a href="http://www.encs.concordia.ca/policies-and-procedures/teaching/full-time-faculty">full-time faculty</a> members.</p>
	<h2>Part-time faculty</h2>
	<p>Find out specific details on workloads for <a href="http://www.encs.concordia.ca/policies-and-procedures/teaching/part-time-faculty">part-time faculty</a> members.</p>
	
	<h2>Application for part-time teaching</h2>
	<p>Download the application form, read the CUPFA deadlines, and find out more about how to <a href="http://www.encs.concordia.ca/policies-and-procedures/teaching/application-for-part-time-teaching">apply for part-time teaching</a> in the Faculty of Engineering and Computer Science. </p>
	<h2>Professional Engineering Licence</h2>
	<p>ENCS faculty members/instructors are encouraged to apply for their <a href="http://www.encs.concordia.ca/policies-and-procedures/teaching/professional-engineering-license">Professional Engineering Licence</a> from the Ordre des ing&eacute;nieurs du Qu&eacute;bec as early as possible in their teaching career.</p>
	
	<h2>Teaching assistants</h2>
	<p>Find out where to apply to become a <a href="teaching-assistants/index.html">Teaching Assistant</a>; learn more about orientation workshops; read the guidelines for completing the Concordia Oral Proficiency Test for Teaching Assistants.</p>
	
	<h2>Teaching excellence award</h2>
	<p>Look at a list of past winners of the <a href="http://www.encs.concordia.ca/policies-and-procedures/teaching/teaching-excellence-award/">ENCS teaching excellence award</a> and learn how to nominate a full- or part-time faculty member for an upcoming award.</p>
	
