<!-- InstanceBeginEditable name="MainContent" -->The Centre for Engineering  in Society is the site of complementary engineering education in the Faculty of  Engineering and Computer Science. Our goal is to assist students as they  develop communication skills, ethical reasoning and an understanding of the  relationship between technology and society on a global scale. Read our mission  and vision statement <a href="http://www.encs.concordia.ca/ces/mission">here</a>. 

  <h2>Teaching</h2>
  <p>Our courses focus on core  skills identified by the Canadian Engineering Accreditation Board and in the  Concordia Strategic Plan, communication, analytical and synthetic thinking,  professionalism, and ethical practice. Through these courses, students gain an appreciation  of how their skills influence social  change. Our courses are listed <a href="http://www.encs.concordia.ca/ces/courses">here</a>.</p>
  <h2>Research</h2>
  <p>Our faculty research takes an interdisciplinary  approach to the public, policy, social, and ethical factors that shape our  relationship to technology. Learn more about faculty research <a href="http://www.encs.concordia.ca/ces/research">here</a>.</p>
  <p>Research opportunities in CES are available to  highly motivated and academically sound students. We welcome inquiries from  students interested in exploring technology-related questions in global  engineering, rhetoric, technical communication, risk, and the social impact of  engineering. More information about graduate and undergraduate research  opportunities is available <a href="http://www.encs.concordia.ca/ces/special-individualized-program">here</a>. 
  <h2>Service</h2>
  <p>To help the Faculty  community stay abreast of the latest best practices in complementary  engineering education, we host the occasional lecture series, <em>Topics in Engineering Education.</em>  Find recent  lectures <a href="http://www.encs.concordia.ca/ces/news-and-events">here</a>. </p>
  <p> We  also administer the <a href="http://www.encs.concordia.ca/ces/engineering-writing-test/">Engineering Writing Test</a>.</p>
