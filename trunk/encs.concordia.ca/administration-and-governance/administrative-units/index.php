  <p>Follow these links to find out more about the individual administrative units of the Office of the Dean, within the Faculty of Engineering and Computer Science, and to learn who to contact to get more information about the unit in question.</p>
  
  <h2>Academic Information Technology Services</h2>
  <p>The <a href="http://www.encs.concordia.ca/administration-and-governance/administrative-units/academic-information-technology-services/">Academic Information Technology Services</a> (AITS) unit is is responsible for developing and distributing IT services to the Faculty of Engineering &amp; Computer Science.</p>
  
  <h2>Academic and Personnel Affairs</h2>
  <p>The <a href="http://www.encs.concordia.ca/administration-and-governance/administrative-units/academic-and-personnel-affairs/">Academic and Personnel Affairs</a> unit focuses on the instructional aspect of academic administration and coordinates full-time academic personnel activities in accordance with CUFA and CUPFA collective agreements and University policies. It is also responsible for implementing Faculty procedures that relate to Human Resources policies.</p>
  
  <h2>Budget Office</h2>
  <p>Check out the <a href="http://www.encs.concordia.ca/administration-and-governance/administrative-units/budget-office/">Budget Office</a> page to find out more about this unit, which is responsible for designing and implementing Faculty procedures that  relate to the policies of Financial Services, Human Resources, and  Purchasing Services.</p>
  
  <h2>Building Operations</h2>
  <p>The main goal of the <a href="http://www.encs.concordia.ca/administration-and-governance/administrative-units/building-operations/">Building Operations</a> unit is to manage all ENCS space within the  university. This includes monitoring of space utilization by the  various departments, assessing requests for space and making  recommendations to the Dean for review and approval.</p>
<h2>Research Office</h2>
  <p>The <a href="research-office/index.html">Research Office</a> assists the Office of the Dean with the management  of internal funding programs, and the review of external research grant  applications and contracts for Faculty approval.</p>
  
  <h2>Student Academic Services</h2>
  <p>The <a href="student-academic-services/">Student Academic Services</a> unit is mandated, along with the individual academic departments in the  Faculty, to look after the needs of both the current undergraduate and  current graduate students in the Faculty of Engineering and Computer  Science.</p>
