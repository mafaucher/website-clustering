<p><em>Elite athlete with a passion for ecological conservation heads to Oxford</em></p> <p><strong>MONTREAL, November 29, 2011</strong> - Michael Noonan, an undergraduate student at Concordia University, has been selected as a 2012 Rhodes Scholar. Rhodes Scholarships provide all expenses for two or three years of study at the prestigious University of Oxford in England, valued at approximately $50,000 per year.<br /><br />The 24-year-old native of Chateauguay is completing a BSc in Honours Ecology with a current Grade Point Average of 4.17 (perfect GPA is 4.3). Already published, Noonan is first author of an article in the world's top-ranked fisheries journal <em>Fish and Fisheries.</em> With an undergraduate degree in ecology, focusing on fish behaviour and conservation in particular, he plans to continue his research pursuing a DPhil in Zoology at the University of Oxford within the Wildlife Conservation Research Unit (WildCRU).<br /><br />The scholarship also requires evidence of physical vitality, leadership, and service, whether in sports, theatre, music or other pursuits. Ranked an elite athlete in the province of Quebec, Noonan has competed for Canada at a number of international wrestling tournaments, and claimed a team national title at the varsity level. He has also worked with the 78th Fraser Highlanders at the Stewart Museum, demonstrating military tactics and the regiment's history.<br /><br />&ldquo;Michael is a true example of the Rhodes philosophy, which requires that the candidates be well-rounded individuals, having shown not only academic excellence, but also active engagement in their community and evidence of a strong commitment to leadership in improving the state of the world,&rdquo; says Concordia President Frederick Lowy.<br /><br />Noonan is one of two Quebecers&nbsp; (11 Canadians) to be selected for the scholarship this year, along with <a href="../../../../../websitewww.salledepresse.uqam.ca/communiques-de-presse-2011/1919-philippe-andre-rodriguez-etudiant-a-luqam-recoit-la-prestigieuse-bourse-rhodes.html">Philippe-Andr&eacute; Rodriguez</a> who is completing a BA in international relations and international law at UQAM and wishes to pursue an DPhil in Politics at Oxford. They join an international group of approximately 80 scholars chosen from fourteen jurisdictions.<br /><br />The Rhodes Scholarships, oldest of the international study awards available to Canadian students, were created in 1902 by the Will of Cecil Rhodes, British philanthropist and industrial magnate.<br /><!--[if gte mso 9]><xml>
