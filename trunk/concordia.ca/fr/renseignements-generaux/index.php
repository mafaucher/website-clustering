  <h2 style="margin-top: 0"><a href="futurs-etudiants/">Envie d&rsquo;&eacute;tudier &agrave; Concordia? </a></h2>
  <p>Concordia est le premier choix des &eacute;tudiants ouverts sur le monde, soucieux de recevoir une &eacute;ducation hors pair. Elle vise &agrave; &ecirc;tre reconnue, au pays et &agrave; l&rsquo;&eacute;tranger, comme l&rsquo;une des meilleures universit&eacute;s canadiennes.</p>
    <ul>
      <li><strong>Premier dipl&ocirc;me en vue</strong> &ndash; Parcourez le panorama des <a href="futurs-etudiants/etudes-de-1er-cycle/index.html">&eacute;tudes de 1er cycle</a> ou consultez directement la liste des <a href="futurs-etudiants/etudes-de-1er-cycle/programmes-de-1er-cycle/index.html">programmes de 1er cycle</a>.</li>
      <li><strong>Deuxi&egrave;me dipl&ocirc;me en vue</strong> &ndash; Parcourez le panorama des <a href="futurs-etudiants/etudes-de-2e-et-3e-cycles/index.html">&eacute;tudes sup&eacute;rieures</a> ou consultez directement la liste des <a href="futurs-etudiants/etudes-de-2e-et-3e-cycles/programmes-de-2e-et-3e-cycles/index.html">programmes de 2e et 3e cycles</a>.</li>
      <li><strong>&Eacute;ducation permanente</strong> &ndash; D&eacute;couvrez nos <a href="futurs-etudiants/education-permanente/index.html">programmes de formation continue</a> et d&rsquo;autres possibilit&eacute;s d&rsquo;apprentissage &agrave; distance.</li>
      <li><strong>Exp&eacute;rience professionnelle</strong> &ndash; Alternez trimestres de cours et stages r&eacute;mun&eacute;r&eacute;s gr&acirc;ce &agrave; nos <a href="futurs-etudiants/enseignement-cooperatif/index.html">programmes d&rsquo;enseignement coop&eacute;ratif</a>.</li>
    </ul>

    <p><strong>Exigences d&rsquo;admission</strong> &ndash; Renseignez-vous sur les conditions d&rsquo; <a href="futurs-etudiants/etudes-de-1er-cycle/exigences-dadmission/index.html">admission au 1er cycle</a> ou d&rsquo; <a href="futurs-etudiants/etudes-de-2e-et-3e-cycles/exigences-dadmission/index.html">admission aux cycles sup&eacute;rieurs</a>. Vous pouvez &eacute;galement consulter les liens suivants :
 <a href="futurs-etudiants/etudiants-internationaux/index.html">&eacute;tudiants internationaux</a>, <a href="futurs-etudiants/etudiants-libres/index.html">&eacute;tudiants libres</a>, <a href="etudiants-actuels/consultation-pedagogique/readmission/index.html">r&eacute;admission des anciens &eacute;tudiants</a> ou <a href="futurs-etudiants/etudiants-visiteurs/index.html">&eacute;
    tudiants visiteurs</a>.</p>
	 
    <p><strong>Pr&ecirc;t &agrave; faire votre choix?</strong> Suivez notre <a href="futurs-etudiants/demande-dadmission/index.html">processus de demande d&rsquo;admission</a>.</p>
   
	 
	 <h2><a href="etudiants-actuels/">D&eacute;j&agrave; inscrit &agrave; Concordia?</a></h2>
    <ul>
      <li><a href="etudiants-actuels/services-aux-nouveaux-etudiants/index.html">Nouveaux &eacute;tudiants</a> &ndash; Renseignez-vous sur les s&eacute;ances d&rsquo;orientation, les ateliers et les programmes sp&eacute;cialement con&ccedil;us pour vous.</li>
      <li><a href="etudiants-actuels/consultation-pedagogique/">D&eacute;sorient&eacute; &agrave; propos des exigences universitaires ou du processus d&rsquo;inscription?</a> Consultez les ressources mises &agrave; votre disposition.</li>
      <li><a href="etudiants-actuels/services-aux-etudiants/index.html">Besoin d&rsquo;aide?</a> Ce ne sont pas les services qui manquent!</li>
      <li><a href="etudiants-actuels/ssociations-etudiantes/index.html">Envie de participer &agrave; la vie &eacute;tudiante?</a> Consultez la liste des clubs et associations.</li>
      <li><a href="http://www.concordia.ca/fr/renseignements-generaux/etudiants-actuels/ressources-generales/">Campus : mode d&rsquo;emploi</a> &ndash; Pour vous y retrouver parmi toutes les ressources du campus.</li>
      <li><a href="http://www.concordia.ca/fr/renseignements-generaux/etudiants-actuels/bureaux-administratifs/">Questions administratives</a> &ndash; Trouvez le service appropri&eacute;.</li>
      <li><a href="etudiants-actuels/diplome-et-gestion-de-carriere/index.html">Bient&ocirc;t dipl&ocirc;m&eacute;?</a> Demandez conseil : orientation professionnelle, recherche d&rsquo;emploi&hellip;</li>
      <li><a href="http://www.concordia.ca/services/ask/">Conseiller en ligne (en anglais)</a> &ndash; Recevez une r&eacute;ponse imm&eacute;diate &agrave; vos questions.</li>
