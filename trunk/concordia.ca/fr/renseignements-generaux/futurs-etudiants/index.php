<h2><a href="etudes-de-1er-cycle/index.html">&Eacute;tudes de 1<sup>er</sup> cycle</a></h2>
<p>Nos quatre facult&eacute;s offrent quelque 300 <a href="http://www.concordia.ca/fr/renseignements-generaux/futurs-etudiants/etudes-de-1er-cycle/programmes-de-1er-cycle/diplomes/">programmes de
baccalaur&eacute;at et de certificat</a> : d&eacute;couvrez celui qui vous
convient ou consultez la <a href="http://www.concordia.ca/fr/renseignements-generaux/futurs-etudiants/etudes-de-1er-cycle/programmes-de-1er-cycle/index-des-disciplines/">liste des programmes par discipline et par
ordre alphab&eacute;tique.</a> </p>

<p>Exigences d&rsquo;admission, droits de scolarit&eacute; et autres
frais, ressources mises &agrave; votre disposition sur nos deux campus,
vie &eacute;tudiante &agrave; Concordia, etc. : en savoir plus sur les
&eacute;tudes de 1<sup>er</sup> cycle.</p>
<br />

<h2><a href="etudes-de-2e-et-3e-cycles/index.html">&Eacute;tudes de 2e et 3e cycles</a></h2>
   
<p>Nos quatre facult&eacute;s et l&rsquo;&Eacute;cole des
&eacute;tudes sup&eacute;rieures offrent, &agrave; temps plein ou
&agrave; temps partiel, plus de 100 <a href="http://www.concordia.ca/fr/renseignements-generaux/futurs-etudiants/etudes-de-2e-et-3e-cycles/programmes-de-2e-et-3e-cycles/index-des-disciplines/">programmes d&rsquo;&eacute;tudes
sup&eacute;rieure</a>s : doctorats, ma&icirc;trises, dipl&ocirc;mes et
certificats.</p>

<p>Exigences d&rsquo;admission, droits de scolarit&eacute; et autres
frais, ressources mises &agrave; votre disposition sur nos deux campus,
vie &eacute;tudiante &agrave; Concordia, etc. : en savoir plus sur les
<a href="etudes-de-2e-et-3e-cycles/index.html">&eacute;tudes de 2<sup>e</sup> et 3<sup>e</sup> cycles.</a></p>
<br />

	<h2><a href="education-permanente/index.html">&Eacute;ducation permanente</a></h2>
	<p>Concordia propose des cours et programmes de formation
	professionnelle non cr&eacute;dit&eacute;s dans de nombreux domaines
	: technologies de l&rsquo;information-for-yourmation, gestion,
	commerce &eacute;lectronique, communications, relations publiques,
	gestion des ressources humaines, anglais (langue seconde)&hellip;
	Sans oublier l&rsquo;apprentissage &agrave; distance et en ligne ou
	encore les programmes pour cadres.</p> 
	
	<p>En savoir plus sur <a href="education-permanente/index.html">l&rsquo;&eacute;ducation permanente.</a></p>
	<br />
	
	<h2><a href="enseignement-cooperatif/index.html">Enseignement coop&eacute;ratif</a></h2>
	<p>Parce qu&rsquo;il combine trimestres d&rsquo;&eacute;tudes et
	stages r&eacute;mun&eacute;r&eacute;s dans le secteur public ou
	priv&eacute;, l&rsquo;Institut d&rsquo;enseignement
	coop&eacute;ratif vous permet d&rsquo;acqu&eacute;rir de
	l&rsquo;exp&eacute;rience professionnelle dans votre domaine tout en
	poursuivant un programme au sein de nos quatre facult&eacute;s et de
	l&rsquo;&Eacute;cole des &eacute;tudes sup&eacute;rieures.</p>
	
	<p>En savoir plus sur nos <a href="enseignement-cooperatif/index.html">programmes d&rsquo;enseignement
	coop&eacute;ratif.</a></p>
	<br />
	
	<h2><a href="etudiants-internationaux/index.html">&Eacute;tudiants internationaux</a></h2>
	<p>Concordia accueille les candidatures en provenance du Canada, des
	&Eacute;tats-Unis et des quatre coins du monde. Des 38 809
	&eacute;tudiants inscrits &agrave; des cours cr&eacute;dit&eacute;s,
	environ 12 % viennent de 150 pays diff&eacute;rents.</p>

	<p>En savoir plus sur les <a href="etudiants-internationaux/index.html">&eacute;tudiants internationaux.</a></p>
	
	<br />
	<h2><a href="etudiants-visiteurs/index.html">&Eacute;tudiants visiteurs</a></h2>
	<p>&Agrave; condition d&rsquo;obtenir de leur &eacute;tablissement
	d&rsquo;attache l&rsquo;autorisation par &eacute;crit de suivre des
	cours &agrave; Concordia, les &eacute;tudiants inscrits &agrave; une
	autre universit&eacute; peuvent obtenir le statut
	d&rsquo;&eacute;tudiant visiteur. </p>
	
	<p>En savoir plus sur les <a href="etudiants-visiteurs/index.html">&eacute;tudiants visiteurs.</a></p>
	<br />
	
	<h2><a href="etudiants-libres/index.html">&Eacute;tudiants libres</a></h2>
	<p>Que ce soit pour votre culture personnelle, pour votre
	carri&egrave;re ou pour avoir une meilleure id&eacute;e de ce qui
	vous attend avant de vous inscrire &agrave; un programme de 1er
	cycle ou d&rsquo;&eacute;tudes sup&eacute;rieures, vous pouvez faire
	une demande d&rsquo;admission en tant qu&rsquo;&eacute;tudiant
	libre. Vous aurez ainsi la possibilit&eacute; de suivre des cours
	individuels &agrave; temps partiel, sans toutefois obtenir de
	dipl&ocirc;me ou de certificat. </p>
	
	<p>En savoir plus sur les <a href="etudiants-libres/index.html">&eacute;tudiants libres.</a></p>
	
	<br />
	
	<h2>Autres options</h2>
	<p>Conform&eacute;ment aux principes g&eacute;n&eacute;ralement accept&eacute;s par les autres universit&eacute;s qu&eacute;b&eacute;coises, Concordia permet &agrave; toute personne admissible de s&rsquo;inscrire au programme de son choix, dans la mesure du possible.</p>
	<p>En savoir plus sur l&rsquo;admission &agrave; l&rsquo;Universit&eacute; en tant qu&rsquo;&eacute;tudiant adulte (&acirc;ge minimum : 21 ans), sur la r&eacute;admission et sur le transfert &agrave; Concordia.</p>

