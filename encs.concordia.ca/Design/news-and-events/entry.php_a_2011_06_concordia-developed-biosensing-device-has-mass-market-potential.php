		<div class="entry-content">
			<div class="entry-body">
				
The presence of potentially harmful foreign substances in our food has long been a source of worry for many Canadians. Thanks to researchers from Concordia University's <a href="http://www.me.concordia.ca/">Department of Mechanical and Industrial Engineering</a>, detecting those substances just got a little easier.<br /><br />Professor Muthukumaran Packirisamy, with invaluable help from his dedicated team, has spent the past two years perfecting a tiny device that can be used commercially to detect the presence of bovine growth hormones in milk. This microfluidic biosensing device was developed with the support of Quebec's <a href="http://www.mdeie.gouv.qc.ca/accueil/">Ministère de dévélopment économic, innovation et exportation</a> (MDEIE) and local company <a href="http://www.gestionvaleo.com/">Gestion Valeo</a>.<br /><br /><div align="center"><img alt="muthu.jpg" src="http://newsfeed.encs.concordia.ca/encs_test/muthu.jpg" class="mt-image-center" style="text-align: center; display: block; margin: 0pt auto 20px;" width="440" height="350" /><i>Dr. Packirisamy and Dean Drew present Valeo representatives with the prototype</i><br /></div><br />Having recently completed the prototype for the groundbreaking device, Packirisamy and his colleagues from the Faculty of Engineering and Computer Science, as well as from Concordia's Office of Research, invited representatives from Gestion Valeo to Concordia's downtown campus for a special presentation. Dr. Sabin Boily, the company's President and CEO, was happy to attend and happier still to be able to hold the new device in his hands. "It is a privilege to receive the prototype, which is the result of two years of hard work by Dr. Packirisamy and his team," said Boily during the afternoon reception. <br /><br />With its mandate to enhance the commercial exploitation potential of university research results, Gestion Valeo was the perfect partner to help bring the device all the way from conceptualization to actualization. Boily was happy to note that the project was well received during a recent presentation to potential industrial partners in Boston. One representative from NASA's mechanical engineering group was particularly interested in the device, seeing the potential of this small, light, portable and safe device to be used to detect such contaminants as bacteria and pathogens in future space missions. With such a positive reception, it seems like there is nowhere for this device to go but up!<br /><div><br /></div>

