  <p>As the proud home of several Canada Research Chairs, Concordia University Research  Chairs, and Research Centres, our Faculty is recognized nationally  and internationally for the high and innovative quality of its  individual and team research. These research strengths are  integral to our goal of providing leading-edge graduate education and  training across our PhD and Master&rsquo;s programs. </p>
  
  <p>In this section of the site, you can find out more information about:</p>
  
  <h2>Faculty Expertise</h2>
  <p>If you want to know the research interests of the professors of the Faculty of Engineering and Computer Science, look no further than our <a href="faculty-expertise-guide/">Faculty Expertise Guide</a>, sortable by research interest and Department.</p>
  
  <h2>Research Chairs</h2>
  <p>The Faculty is home to over 20 <a href="http://www.encs.concordia.ca/research/chairs-and-awards/">Research Chairs</a>. This page will give you more information on the individuals and their research projects.</p>
  
  <h2>Research Centres</h2>
  <p>Nine different research <a href="http://www.encs.concordia.ca/research/centres/">centres</a> are affiliated with Concordia's Faculty of Engineering and Computer Science. This page gives you a brief summary of each centre, plus a links to all the offical websites.</p>
  
