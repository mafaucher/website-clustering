  <h3>PAST LECTURES</h3>
 
 <div class="researchchair">
<div><span class="toggler"></span>Jason Foster: <span class="field">Developing the non-fungible, socially aware, civic-minded engineer</span></div>
<p>
Thursday, February 17<br />
1 - 2 p.m.<br />
EV 3.309<br />
<br />
<strong>Abstract</strong> In many of their foundation courses our students solve well- and pre-defined problems. Unfortunately, problems such as these rarely occur in professional practice. Our graduates may also find themselves stereotyped, either as fungible commodities or as engineers disconnected from the social systems they both inhabit and design. To be successful, our future graduates must bring a unique, individual, and socially aware perspective to their identification, framing, and solution of complex problems.<br />
<br />
Cities, especially those with a rich multicultural history, provide an
ideal environment in which students can learn about, and practice, engineering design. Such cities are simultaneously easily accessible and almost infinitely complex, house a varied and continually evolving mix of stakeholders, and cannot be understood without integrating multiple disciplines and perspectives.
<br />
This talk will discuss how a city-based, student-driven design project has prompted first year students to explore their beliefs about the nature and practice of engineering design. The project has provided students the opportunity to share their visions with a broad audience including their professors, city representatives, and the media, all while challenging the students&#8217; and their instructors&#8217; understanding of what it means to be a citizen and a unique engineer.<br />
<br />
<strong>Biosketch</strong>
Jason Foster is the Senior Lecturer in Engineering Design Education in the Division of Engineering Science at the University of Toronto. His formal training was in Systems Design Engineering, and he has worked as a professional software developer and corporate software trainer. Jason returned to the academy to focus on understanding and improving undergraduate engineering design education. His teaching and research interests lie at the intersection of systems, design, and education. In the classroom, Jason focuses on helping his students (re)define their own beliefs about, and approaches to, the practice of engineering design. Key to Jason's pedagogical approach is making students responsible for finding and framing the engineering problems that they will then solve. He further requires that the problems they identify come from a focused, critical, and multi-perspective exploration of their surroundings.
<br />
<br />
Jason's work has been presented at conferences include the American Society for Engineering Education and Engineering Education for Sustainable Development, and he has enjoyed bridging the engineering design and liberal studies communities at those gatherings and at his home institution.</p></div>
