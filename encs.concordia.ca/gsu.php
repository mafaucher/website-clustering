This talk explored the contours of engineering ethics, offering both a historical explanation of engineering ethics, and an argument for a new way to think about the link between ethics and engineering. It answered the historical question, &quot;In what ways has a concern for ethics been incorporated into the development of the profession of engineering in the past?&quot; as well as the prospective question, &quot;What might the future of the relationship be-tween ethics and engineering look like?  The historical account involves the development of professional guilds at the beginning of the twentieth century, the articulation of a code meant to insure working engineers were acting virtuously, and a century riddled with ex-amples of destruction, danger, and death aided and abetted by technological advance.  The argument advanced assumes that ethics can be taught to engineers as a matter of de-liberation and communication, and that instruction in these practices improves the moral character of the student.  The code of ethics, which was intended to guide engineers practice in earlier decades, is no longer enough to guide engineering practice in the twenty-first century.<br />
