Significant forces are impacting engineering education due to globalization, pressure within the engineering profession, and in higher education. Recommendations from engineering bodies, international groups, and the learning sciences are pushing engineering programs to offer a greater variety of learning experiences. This talk discussed these forces and approaches taken to implement these ideas within undergraduate engineering programs.<br />
<br />
Brian Frank is an associate professor in electrical engineering, and the DuPont Canada Chair in Engineering Education Research and Development. He is coordinating the Engineering Graduate Attribute Development Project sponsored by the National Council of Deans of Engineering and Applied Science. His educational interests are in complex problem solving, assessment, and educational technology.<br />
