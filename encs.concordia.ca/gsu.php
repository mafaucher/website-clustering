  <h2><a name="About"></a><br />
    About</h2>
  <p>The General Studies Unit (GSU) is located within the Faculty of Engineering and Computer Science. It has been created with two major objectives: </p>
  <ul>
    <li>The primary objective of the GSU is to blend the teaching of engineering and technology with skills needed for all engineering and computer science students in the Faculty to become professionals who are responsible, articulate and ethical. We focus on: 
      <ul>
        <li>Communication (Language and Technical)<br />
          </li>
        <li>Critical Thinking and Innovation<br />
          </li>
        <li>Impact of Engineering and Technology on Society<br />
          </li>
        <li>Professionalism and Ethics in Engineering<br />
          </li>
        <li>Engineering, Policy and Risk</li>
      </ul>
      </li>
    <li>An additional objective of the GSU is to coordinate and manage those graduate and undergraduate courses in engineering and computer science that are common to multiple departments that are intra Faculty or inter Faculty.<br />
    </li>
  </ul>
  <h3>Graduate Studies</h3>
  <p>At this stage, no separate degree programs are offered by GSU. However, highly motivated and research minded students can be supervised by GSU faculty members under the School of Graduate Studies&rsquo; <a href="http://graduatestudies.concordia.ca/SIP/">special individualized programs</a> (SIP). Emphasis is on interdisciplinary research at the graduate level. In addition, GSU faculty currently co-advise students in BCEE, ECE and CIISE.<br />
  </p>
  <h2><a name="EWT"></a><br />
    Engineering Writing Test</h2>
  <p>All students in the Faculty of Engineering and Computer Science must satisfy the Faculty writing skills requirement. They have two options for fulfilling this requirement:</p>
  <ol>
    <li>Take the Engineering Writing Test (also known as EWT)<br />
      - 
or -</li>
    <li> Take ENCS 272 (a three credit course) and pass with a C- or better.</li>
    </ol>
  <p>The above writing skill requirement should be fulfilled within the first 30 credits of a student's program. Failure to do so will delay the completion of ENCS 282, which is a core requirement to all engineering students, and a necessary pre-requisite for 400 level courses.</p>
  <p>For details on the EWT (including how to register, how frequently the test is offered, how to prepare, and what to do in the case of failure, please refer to the <a href="https://fis.encs.concordia.ca/challenge/servlet/Login">EWT website</a>.</p>
  <h2><a name="Courses"></a><br />
    Courses</h2>
  <p>The GSU brings complementary skills and knowledge to engineering and information technology training. Critical thinking, rhetoric, technical writing, oral communication and presentation skills, professionalism, social responsibility and ethics are important building blocks for competent engineers and information technology specialists.</p>
  <p>The GSU develops and teaches courses related to topics that will prepare engineering and information technology students for their professional practice. Courses are offered both at the undergraduate level and at the graduate level.  Most of these courses (except ENCS 272 and 283) are required core courses at the undergraduate level. They are taken by four to six hundred students each year and they are offered in multiple sections in all three semesters.</p>
  <h3>Undergraduate </h3>
  <ul>
    <li> ENCS 272	Compositions and Argumentation for Engineers   (3 Credits)</li>
    <li>ENCS 282	Technical Writing and Communication   (3 Credits)</li>
    <li>ENCS 283	Innovations and Critical Thinking in Science and Technology   (3 Credits)</li>
    <li>ENGR 201	Professional Practice and Responsibility    (1.5 Credits)</li>
    <li>ENGR 392	Impact of Technology on Society   (3 Credits)</li>
    <li>ENGR 393  	Impact of Information Technology on Society (3 credits)  - This course is intended for Computer Science Students</li>
    </ul>
  <p>For course descriptions, see Section 71.60 of the <a href="http://registrar.concordia.ca/calendar/">Undergraduate Calendar</a>.</p>
  <h3>Graduate </h3>
  <ul>
    <li>ENCS 5721	Composition and Argumentation for Engineers   (3 Credits)</li>
    <li>ENCS 6041	Creativity, Innovation and Critical Thinking   (3 Credits)</li>
    <li>ENCS 6721	Technical Writing and Research Methods for Engineers   (3 Credits)</li>
    </ul>
  <p>For course descriptions, see <a href="http://graduatestudies.concordia.ca/publications/graduatecalendar/">the Graduate Calendar</a>.</p>
  <h2><a name="Research"></a><br />
    Research</h2>
  <p>Research opportunities in GSU are available to highly motivated and academically sound students who have shown some ability to do research.  Focus of research in GSU is interdisciplinary in nature and the field of research for individual faculty members is described below. Applications will be considered for doctoral studies under the SIP or <a href="http://graduatestudies.concordia.ca/SIP/">Special Individualized Program of the School of Graduate Studies</a>. Exceptional cases will be considered also for the research based Master&rsquo;s program under SIP.</p>
  <p>Interested students are encouraged first to send their detailed CV along with a short research proposal directly to one of the GSU professors depending on the area of the student&rsquo;s proposed research.</p>
  <h3>Faculty research profiles  </h3>
  <p><a href="http://encs.concordia.ca/resources/faculty-and-staff-directory/details/?id=Dysart-Gale, D.&d=08">Deborah Dysart-Gale</a>&rsquo;s research brings together her interests in intercultural communication, end-of-life health communication and community development. She works primarily to improve health and social work communication in developing communities. Her recent interest is in applying health communication scholarship to ICT projects for social development.</p>
  <p><a href="http://encs.concordia.ca/resources/faculty-and-staff-directory/details/?id=Mudry, J.&d=08">Jessica Mudry</a> is an interdisciplinary researcher and draws from the fields of rhetoric of science, food studies, science communication and the history of science and medicine.  Her current project focuses on 19th century German and American nutrition research, and the roles of technologies in forming discourses of nutrition.</p>
  <p><a href="http://users.encs.concordia.ca/~rdanisch/">Robert Danisch</a>&rsquo;s research interests are in the field of rhetorical studies.  His work investigates the history, theory, and practice of public argument, democratic deliberation, rhetorics of science, and risk communication.  He is interested in the role that rhetorical practices play in forming public cultures, especially in relationship to the tradition of American pragmatism and the history of forms of democratic citizenship.</p>
<p><a href="http://users.encs.concordia.ca/~kschmitt/">Ketra Schmitt</a> is interested in science, technology and public policy issues that have important risk-benefit tradeoffs.  She develops methods and builds systems models to characterize and analyze risks and benefits as well as the associated uncertainty.  Examples of her research include characterizing the economic impacts associated with terrorism and natural disasters and combining information from disparate health studies for environmental contaminants. </p>
  <p><a href="http://users.encs.concordia.ca/~govind/">Govind Gopakumar</a>&rsquo;s major research interest is related to infrastructures of cities such as technological networks of information, water supply, sewers, roads, bridges, and energy. He is particularly interested in investigating the dynamics of the interaction of global and urban/regional factors on processes of change in urban infrastructures in developing countries. His doctoral degree is in Science and Technology Studies from Rensselaer Polytechnic Institute.</p>
