  <p>To help you navigate the diverse policies and procedures found within the Faculty of Engineering and Computer Science, we've compiled some helpful information in the areas of academic personnel, budget and finance, research funding, and teaching.</p>
  <p>In this section of the site, you can find out more about the following areas:</p>
  
  <h2>Academic personnel</h2>
  <p>If you want to learn more about how to apply for tenure, renew your contract, pursue promotion to associate or full professor, or how to apply for a sabbatical, check out these <a href="academic-personnel/">pages on academic personnel policies</a>. You'll also find a detailed critical path, listing all the important dates for faculty members.</p>
  
  <h2>Budget and finance</h2>
  <p>Looking for more information about what financial support is available for faculty? Want to check the rules related to expenses before leaving on a research trip? Thinking of purchasing new books through the Professional Development Fund? Visit the <a href="http://www.encs.concordia.ca/policies-and-procedures/budget-and-finance/">budget and finance area of the site</a> to find out more.</p>
  
  <h2>Research support</h2>
  <p>The Faculty's Research Office can provide you a wealth of information on such funding opportunities as CARA funds, the Graduate Student Support Program, and Start-Up Funds for new faculty members. Check out the<a href="http://www.encs.concordia.ca/policies-and-procedures/research-support/"> research support pages</a> to find out more.</p>
  
  <h2>Teaching</h2>
  <p>Whether you're a faculty member or a teaching assistant, these <a href="http://www.encs.concordia.ca/policies-and-procedures/teaching/">pages on teaching</a> will prove invaluable to you, as they include important lists of dates for all instructors as well as important guidelines for part-time teachers and teaching assistants. You can also find out more about how to nominate someone for the Teaching Excellence Award.</p>
  
