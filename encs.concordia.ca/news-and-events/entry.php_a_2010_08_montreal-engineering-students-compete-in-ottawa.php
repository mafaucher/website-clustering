			<div class="entry-meta" title="2010-08-11T16:49:26-05:00">Wednesday Aug 11, 2010</div>
		</div>
	
		<div class="entry-content">
			<div class="entry-body">
				
Engineering student <b>Mohammed Abukhalid</b> will be heading to Ottawa on September 10 to present at The <a class="zem_slink" href="http://www.theiet.org/" title="Institution of Engineering and Technology" rel="homepage">Institution of Engineering and Technology</a>'s (IET) Americas Regional Present Around the World (PATW) competition. On May 4, Abukhalid won a local Montréal competition where students presented science and engineering topics to a panel of judges. The runner up of the local competition was Santiago Paiva, a student from Vanier College, who will also be heading to Ottawa for the PATW competition.<br /><br />Abukhalid's presentation, entitled Cooperative Autonomous Aerial Reconnaissance Vehicles, won at the Montréal competition.&nbsp; He will complete his undergraduate degree in electrical engineering at Concordia University in December 2010 and then plays to stay on to pursue his master's degree. For the past year, he has worked full time as a Flight Controls Specialist for Thales Canada in addition to taking a full course load. He is originally from Palestine and moved to Montréal 17 years ago from Amman, Jordan.<br /><br />Students and young professionals from all over North America will present at the Americas Regional PATW in Ottawa, as they compete for a chance to present at the PATW finals in London, England and receive a cash prize. The PATW competition is a chance for individuals from around the globe to showcase their academic prowess and their ability to communicate technical topics at a comprehensive level.<br /><br />Since its inception in the United Kingdom in 1871, the Institution of Engineering and Technology has become a world-leading professional organization. The IET has more than 150,000 members in 127 countries and offices in Europe, North America, and Asia-Pacific. Providing a global knowledge network to facilitate the exchange of ideas, the IET promotes the positive role of science, engineering, and technology in the world.<br /><br />Video of the event will be <a href="http://patw2010.org/liveblog.php">streamed live</a> on September 11, 2010, at 9:30 Eastern Daylight Time.<br />

