		<div class="entry-content">
			<div class="entry-body">
				
On Wednesday, April 14, ENCS warmly welcomed Montreal Architect <b>Sevag Pogharian</b> for an <a href="http://www.ieee.ca/">IEEE</a>-sponsored lecture on the subject of the Alstonvale Net Zero House. <br /><br /><img alt="IEEE-talk.jpg" src="http://newsfeed.encs.concordia.ca/encs_test/IEEE-talk.jpg" class="mt-image-center" style="text-align: center; display: block; margin: 0pt auto 20px;" height="191" width="440" />A good crowd of faculty, students, and interested individuals gathered for the evening lecture organized by <a href="http://users.encs.concordia.ca/%7Esheldon/"><b>Sheldon Williamson</b></a>, Assistant Professor in the <a href="http://www.ece.concordia.ca/">Department of Electrical and Computer Engineering</a>, with help from his graduate student Chirag Desai. The audience listened attentively as Pogharian described how he is pushing the borders of net zero housing through his creation the Alstonvale Net Zero House<br /><br />In collaboration with individuals from Concordia's Faculty of Engineering and Computer Science, Pogharian is building a house that generates all the energy required for the household's domestic needs; generates all the energy required for the household's local transportation needs; and integrates home-scale agriculture in order to displace the industrial food system and enhance a household's food energy balance. This innovative structure, located in the suburban community of Hudson, Quebec, is one of the winning projects in <a href="http://www.cmhc-schl.gc.ca/en/">Canada Mortgage and Housing Corporation</a>'s <a href="http://www.cmhc-schl.gc.ca/en/en_001.cfm">EQuilibrium Sustainable Housing Demonstration Initiative</a>. <br /><br />Pogharian made an excellent case for a future that features exactly this type of net zero housing, framing his work with the Alstonvale House in the context of the global energy crisis and the need to conserve resources such as water and fossil fuel. He is currently working on the design for an entire net zero neighbourhood, which is set to be built in the <a href="http://www.ville.vaudreuil-dorion.qc.ca/">Vaudreuil-Dorion</a> neighbourhood. Such a project will help bring this new concept of sustainable housing to a wider variety of people and while showing our community that this is the way of the future.<br />Audience members engaged Pogharian in a lively question and answer period following the talk, proving that net zero energy buildings are a hot topic at Concordia and beyond--and will remain one for years to come.<br /><br />For more information on the Alstonvale Net Zero House, as well as on the work of Sevag Pogharian, please visit <a href="http://www.spd.ca/Sevag_Pogharian_Design/Sevag_Pogharian_Design.html">www.spd.ca</a><br /><br /> <div><br /></div>

