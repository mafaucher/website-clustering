		<div class="entry-content">
			<div class="entry-body">
				
<b>Amin Hammad</b>, an associate professor in the Concordia Institute for Information Systems Engineering, has been named the Fulbright Visiting Chair in Transborder Studies at <a class="zem_slink" href="http://maps.google.com/maps?ll=33.4211111111,-111.931666667&spn=0.01,0.01&q=33.4211111111,-111.931666667%20%28Arizona%20State%20University%29&t=h" title="Arizona State University" rel="geolocation">Arizona State University</a>.<br /><br />While at Arizona State University, Hammad will be conducting research on collaborative emergency management of international bridges between Canada and the United States.<br /><br />"It is with great pleasure that I congratulate Amin Hammad as he joins the distinguished group of Canada-U.S. Fulbright scholars," said <b>Judith Woodsworth</b>, President and Vice-Chancellor of Concordia. "Hammad's research while at Arizona State University will certainly offer a unique insight into important issues relating to the security of our shared Canada and U.S. border."<br /><br />By engaging our brightest minds in academic exchanges, the Canada-U.S. Fulbright Program seeks to enhance mutual understanding between Canada and the United States. Through its bilateral academic exchanges, outstanding students, scholars and professionals strengthen Canada-U.S. relations by examining a wide range of subjects which are critical to the relationship between the two countries.<br /><br />Operating in over 150 countries worldwide, the Fulbright program has long been regarded as the world's premiere academic exchange.<br />

