			<div class="entry-body">
				
Research and innovation are both key words for Concordia, so it is fitting that NSERC-Quebec gave the University special recognition for just that, during Association for the Development of Research and Innovation in Quebec (ADRIQ) "Célébrons Le Partenariat" soirée.<br /><br /><img alt="ADRIQ1.jpg" src="http://newsfeed.encs.concordia.ca/encs_test/ADRIQ1.jpg" class="mt-image-center" style="text-align: center; display: block; margin: 0pt auto 20px;" width="440" height="269" /><i><font style="font-size: 0.8em;">The Concordia team from left to right: Bob Fews, Shelley Sitahal, Suong Van Hoa, Dora Iordan, Jonathan Farber. Photo courtesy of André Bazergui</font></i><br /><br />Present at the event, held at the Marché Bonsecours on May 26, were some 300 researchers and entrepreneurs, including VPs of Research from various universities and colleges, representatives from government organizations; legal and financial counsels; commercialization specialists and many more. Suzanne Fortier, President of the Natural Sciences and Engineering Research Council (NSERC), also attended as a guest of honour, representing the national funding body, which was a co-sponsor of the event. <br /><br />Joining these delegates were representatives from Concordia's Faculty of Engineering and Computer Science and Central Office of Research: Professor in Mechanical Engineering Suong Van Hoa (who was himself recognized for a special research partnership with McGill University), Aerospace Special Advisor and Director of the Concordia Institute for Aerospace Design and Innovation Robert Fews, Associate Director for Research Partnerships Shelley Sitahal, Research Facilitator Dora Iordan, and Technology Transfer Officer Jonathan Farber. <br /><br />When Pierre Bourassa, Manager of the NSERC Quebec Regional Office, honoured Concordia with its special award in recognition of the University's impressive success with the NSERC Engage grants this year, it was quite the surprise for the delegation. While it has been a point of pride for the University that it has received 16 of these grants, a number unprecedented for a single institution in Quebec, no one expected that Concordia would be singled out during the soirée. Hopefully this honour forecasts further success for these exciting partnerships!<br /><br />The NSERC Engage grants give Canadian companies the chance to foster the development of new partnerships with researchers at Canadian universities. The grants provide up to $25,000 over 6-months, and support short-term research and development projects aimed at addressing a company-specific problem in the natural sciences or engineering fields. Recipients are chosen based on the creation of new collaborative relationships; research competence as collaborators; industrial relevance; contribution to technology transfer; and benefit to Canada. <br /><br /><!--[if gte mso 9]><xml>
