<img alt="Deborah Dysart-Gale" src="http://newsfeed.encs.concordia.ca/encs_test/DSC_1841.jpg" class="mt-image-left" style="float: left; margin: 0pt 20px 20px 0pt;" height="331" width="220" />The Faculty of Engineering and Computer Science is pleased to announce that an advisory search committee for a Chair for the <a href="../gsu.php">General Studies Unit</a> has completed its work and recommended the appointment of Dr. Deborah Dysart-Gale as Chair of the Department effective June 8, 2010.&nbsp; <br /><br />Dr. Dysart-Gale obtained her PhD in 1999 from the University of Pittsburg and joined <a class="zem_slink" href="http://www.concordia.ca/" title="Concordia University" rel="homepage">Concordia University</a> in 2006 as Assistant Professor in the General Studies Unit.&nbsp; She has been Associate Chair of the GSU since 2007. She has great plans for GSU, including coming up with a more appropriate name for the unit, as well as involving it in departmental activities such a capstone and accreditation. <a href="../administration-and-governance/about-the-dean/index.html">Dean Robin Drew</a> was pleased with the announcement, noting that Dysart-Gale's "consultative style and ability to listen to others are just a couple of the many great attributes that she brings to the job." <br /><br />Dysart-Gale is up for the challenge, pointing out that this faculty has been interested in the social dimensions of engineering since 1968, back before the first Earth Day. "I believe that shows that Concordia was engaged in environmental and social stewardship before many in the general public even realized the seriousness of these issues," she explains. "As these problems have become more acute in recent years, so has ENCS's commitment to educating technically excellent engineers and computer scientists whose work produces excellent social outcomes. The General Studies Unit's teaching, research, and outreach efforts contribute toward that goal by emphasizing the socioeconomic, political and ethical aspects of engineering practice." With a dynamic leader like Dysart-Gale at its helm, the General Studies Unit is sure to take some exciting new directions in the coming years.<span class="zem-script more-related pretty-attribution"><script type="text/javascript" src="http://static.zemanta.com/readside/loader.js" defer="defer"></script></span>

