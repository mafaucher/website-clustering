<p>It's been a busy spring for Concordia's Engineering and Computer Science Alumni Chapter (ECAC). As you'll read below, the special events they organized during Engineering Week in March were well attended and much appreciated, as was the year-end wrap-up in early April. The ECAC is already looking forward to a busy fall. Be sure to keep your eye on their website at alumni.concordia.ca/chapters/faculty/ec/ for more information.</p>
<p><strong>Behind the scenes in space </strong></p>
<p>Dave Schaffer, Senior Robotics Instructor with the Canadian Space Agency, came back to school to celebrate Engineering Week with ECAC and the undergraduate Engineering and Computer Science Students' Association, who cosponsored the event. The ENCS alumnus gave a spellbinding talk to a packed crowd on the evolution of Canada's space program as well as an in-depth look at what it takes to train an astronaut. </p>
<p><img style="TEXT-ALIGN: center; MARGIN: 0px auto 20px; DISPLAY: block" class="mt-image-center" alt="Eng-Week-Events.jpg" src="http://newsfeed.encs.concordia.ca/encs_test/Eng-Week-Events.jpg" width="440" height="316" />Schaffer (pictured above with memberse of the ECAC and ECA executive) graduated from Concordia in 1978 with a Bachelor's in Electrical Engineering and immediately joined Spar Aerospace in Montreal. There, he worked in design as well as systems engineering before eventually transferring to Spar's Toronto offices, where he worked on the first Canadarm. Schaffer eventually joined the Canadian Space Agency as it worked closely with NASA on developing the International Space Station. The project was decades in the making but led to fascinating work and long-lasting friendships, which Schaffer described in compelling detail. He recalled what it was like to be working for the CSA at the time of the Challenger and Columbia accidents and spoke of the many hurdles that had to be surmounted in order to get the Space Station off the ground. </p>
<p>The highlight of the talk was undoubtedly the behind-the-scenes photo and video footage that Schaffer presented. Stunning images of the earth as scene from the International Space Station wowed the audience, as did "home" movies made by the astronauts--some of whom were personally trained by Schaffer himself--of what life is like on the station. For Schaffer, of course, the focus remains on what life is like down on earth, where he says he is always "astounded and surprised by how little Canadians know about our own remarkable space program and achievements." He expressed a sincere hope that more would be done to educate the public and to get the next generation of aerospace engineers excited about exactly this kind of work. An excellent spokesman, Schaffer left his audience with just that.</p>
<p><strong>Software and Society<br /></strong>Another exciting activity organized by the Engineering and Computer Science Alumni Chapter was a fascinating conversation on the subject of software &amp; society led by Peter Grogono, a Professor in the Department of Computer Science and Software Engineering. He subtitled his talk "the good, the bad and the ugly" and began the session with examples from all three of these categories. Questions, answers, comments and postulations were fast-flowing from the interested and informed group that came to the talk prepared to engage with each other as well as with Grogono. The conversation grew and evolved as the discussions touched on subjects ranging from Swiss Army knives to Apple iPhones, and everyone left the conference room with a broadened perspective on software's role within society.</p>
<p><strong>The year--and careers!--in review<br /></strong>On April 8, the ECAC hosted a double-barrelled evening of information. First up was the ECAC's Annual General Meeting, hosted by Véronique Tokateloff, the President of the Chapter's Executive Committee. She took the attendees through a review of the ECAC's past year of exciting events before giving a preview of upcoming happenings scheduled by ECAC.</p>
<p>Following the AGM, it was time for a talk on forging your career path, by Carolyn Fe-Trinidad, founder and President of CFT Inc., a Montreal-based consulting firm that offers a variety of human resources services for the high-tech industry. Audience members listened carefully&nbsp; as Fe-Trinidad went through the dos and don'ts of job hunting in the high-tech world, sharing her experiences and insider know-how gleaned from over 20 years running her own head-hunting business. The evening wrapped up with a lively question and answer session.</p>
<p><strong>Looking ahead<br /></strong>The ECAC is looking forward to seeing engineering and computer science alumni and professors celebrating a milestone anniversary during Homecoming this fall. The annual ENCS Reunion (held for alumni from pre-1960, 1960, 1965, 1970, 75, 80, 85, 90, 95, 2000, &amp; 2005) is scheduled for Saturday, October 2.</p>
