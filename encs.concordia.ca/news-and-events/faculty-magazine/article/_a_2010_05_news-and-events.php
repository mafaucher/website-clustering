<p><strong>Mechtronix Gift Announcement</strong></p>
<p><img style="TEXT-ALIGN: center; MARGIN: 0px auto 20px; DISPLAY: block" class="mt-image-center" alt="Mechtronix_0112.jpg" src="http://newsfeed.encs.concordia.ca/encs_test/Mechtronix_0112.jpg" width="440" height="292" />Co-founders of <a href="http://www.mechtronix.com/Home">Mechtronix World Corporation</a>, Xavier Hervé and Jo Frazao returned to ENCS for a very special occasion in early March: the announcement of a $150,000 donation in support of the renovation of an area affectionately known as the "Cage," located in the basement of the Hall Building. </p>
<p>First established in the 1970s by Frazao and his peers while pursuing undergraduate degrees in engineering here at Concordia, the Cage was built as a workspace for Concordia's <a href="http://www.concordiasae.ca/">Society for Automotive Engineers</a>. Over the years, the Cage grew to become a place not just for SAE but for countless engineering students working on their capstone projects.</p>
<p>The funds from Mechtronix have now transformed the Cage from a dingy subterranean garage into a gleaming centre for automotive innovation, now named the Intelligent Machines World.</p>
<p><strong></strong>&nbsp;</p>
<p><strong>Cisco&nbsp;Gift Announcement</strong></p>
<p><img style="MARGIN: 0px 20px 20px 0px; FLOAT: left" class="mt-image-left" alt="Cisco_0068.jpg" src="http://newsfeed.encs.concordia.ca/encs_test/Cisco_0068.jpg" width="220" height="331" />ENCS was delighted to welcome Nitin Kawale (pictured, left), President of <a href="http://www.cisco.com/web/CA/index.html">Cisco Canada</a>, along with several members of the Cisco team officials on April 7 to officially announce the creation of the new Cisco Network Security Laboratory, made possible by the generous donation of a comprehensive network security solution valued at over $400,000. </p>
<p>The new Cisco Network Security Laboratory is set to become an important part of the <a href="http://www.ciise.concordia.ca/">Concordia Institute for Information Security Engineering</a> by making it possible for the its students to gain practical experience by working in a dedicated space that houses leading-edge equipment and software. The Cisco security solution includes a combination of innovative security appliances and integrated network security, such as a virtual private network, a firewall, remote-workforce security, intrusion prevention and advanced access control. It also includes integrated physical security, Internet Protocol surveillance and emergency-notification systems.</p>
<p>&nbsp;</p>
<p><strong>Exposcience </strong></p>
<p>The 2010 <a href="http://www.exposciencesbell.qc.ca/en/121-expo-regionales.htm">Bell Montreal Regional Science &amp; Technology Fair</a> was co-hosted at Concordia from March 21 through 23 by the Faculty of Engineering &amp; Computer Science and the Faculty of Arts &amp; Science. This year marked the 50th anniversary of science fairs in the province, as well as the first time the event has been hosted by a university.</p>
<p>Over 300 bright young students from high schools and CEGEPs around Montreal came to Concordia for the 3-day event, which was opened by astronaut and MP, the Honourable Marc Garneau. Dean Drew was on hand as the fair wrapped up with a gala awards ceremony, during which he handed out medals and certificates to deserving participants--and gave two particularly scientifically minded youngsters ENCS entrance scholarships worth $2,500.</p>
<p><strong></strong>&nbsp;</p>
<p><strong>K'Nex </strong></p>
<p>As part of the Bell Montreal Regional Science &amp; Technology Fair, a 30 foot representation of Chicago's Sears Tower was constructed in the Harriet and Abe Gold Atrium of the EV Building. The tower used roughly 50,000 pieces of K'Nex.</p>
<p>Last year, James Goggans and his team built a 40 ft model of the Eiffel Tower as the centrepiece of Engineering Week. This year, Goggans--who will receive his BEng this spring--served again as project manager and arranged for the structure to become part of the fair. He, Kirt Cabral (BEng 08), and ECE undergrad James Dennick enlisted the help of the visiting high school and CEGEP students to help them build the structure.</p>
<p><br /><strong>Capstone </strong></p>
<p>March 25 and 26 marked the annual Capstone Exhibition, during which final-year ENCS undergraduates present their year-end projects. The atrium of the EV building was taken over by displays on subjects ranging from solar carports to wind turbines; from redesigning the Decarie Interchange to a pneumatically powered robotic arm. ENCS members and the general public came out in droves to see the fantastic work being done by our undergraduates.</p>
<p>More details, plus a <a href="../../entry.php_a_2010_03_capstone-presentations.php">photo slideshow</a> can be found on our online news archive here.</p>
<p><strong></strong>&nbsp;</p>
<p><strong>MIE Awards</strong></p>
<p>On April 13, the <a href="http://www.me.concordia.ca/">Department of Mechanical and Industrial Engineering</a> held its Annual Design &amp; Awards Day Ceremony to honour overall academic achievement, excellence in teaching, and the best Capstone projects by students in the Department.</p>
<p>Teaching excellence awards were presented to <a href="http://users.encs.concordia.ca/~bhuiyan/">Nadia Bhuiyan</a> and Nabil Esmail, who were both surprised and delighted to be singled out. The prestigious Richard Cheng Design Awards for Mechanical Engineering went to the following teams: Design and Construction of an Atomic Force Microscope - Stephen Dickison, Rafael Arturo Insignares Fernandez, Sarah Halliburton, Gregory Lodge Kramer, Stefan Marchant&nbsp; (supervised by Rolf Wuthrich) and Design of an Experimental Setup for the Evaluation of Blunt Traumatic Aortic Rupture - Paulo Da Silva, Sara Fawal, Jennifer Goehring, Niloofar Moradi-Khaniabadi, Patrick Sardilli, Martin Sosa (supervised by Lyes Kadem).</p>
<p>A full list of winners and photo slideshow can be found on the <a href="../../entry.php_a_2010_04_mie-holds-annual-design-awards-day.php">ENCS website</a>.</p>
<p>&nbsp;</p>
<p><strong>Industrial Advisory Council<br /></strong>ENCS's <a href="http://www.encs.concordia.ca/Design/administration-and-governance/industrial-advisory-council/">Industrial Advisory Council</a>, composed of industry and faculty representatives meets twice a year to help foster excellence in academic programs and research and to develop the Faculty's relationships with industry and various institutions related to engineering and computer science. Its most recent meeting, held April 12, featured presentations from Drs. Mudur and Debbabi on programs and research in gaming at ENCS. Individuals from dynamic software companies, (Martin Walker, Chief Technology Officer for A2M; Chris Young, Head of Engineering Media and Entertainment at Autodesk; and Nick Giannias, Vice-President of Research &amp; Technology and Product Management at Presagis) then gave presentations on the timely topic of video and serious gaming, which inspired the IAC to explore new areas of collaboration between the gaming industry and ENCS. The council's next meeting is scheduled for early autumn, during which the subject of sustainable energies will be on the table. The Council is chaired by Jacques Lyrette, former Vice President of Technology and Industry Support at the National Research Council of Canada and current President of Jacma Management. </p>
