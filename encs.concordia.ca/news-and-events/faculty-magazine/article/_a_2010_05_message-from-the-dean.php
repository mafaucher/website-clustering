<p><img style="MARGIN: 0px 20px 20px 0px; FLOAT: left" class="mt-image-left" alt="Dean_0004.jpg" src="http://newsfeed.encs.concordia.ca/encs_test/Dean_0004.jpg" width="220" height="331" />With Convocation just around the corner and final exams looming large, I'd like to extend my heartfelt congratulations and best wishes to all the students who will be graduating from the Faculty of Engineering and Computer Science this summer. I hope that you'll look back on your accomplishments at Concordia with a feeling of pride--and hope that you'll keep in touch as you move on to careers in industry or academia, or even remain right here at Concordia in pursuit of further education. I know I speak for all of my colleagues when I say that it is our bright and vibrant student body that makes this Faculty what it is.</p>
<p>I'd also like to extend my congratulations to the professors featured on this cover of this issue of the Faculty Quarterly. It seems like only yesterday that I welcomed Drs. Akbari, Gopakumar, Jahinuzzaman, Sabdi and Schiffauerova to ENCS and now here we are, looking back at their first academic year with us at Concordia. The group is a microcosm of this Faculty: they come from around the world and boast backgrounds in sustainability, social aspects of engineering, electrical engineering, software engineering, and innovation systems. Indeed, these 5 new faculty members make great representatives of the cutting-edge teaching and research that are helping ENCS to realize its vision statement: to be recognized globally for excellence in innovative, applied and socially relevant research as well as high quality and comprehensive curricula that provides graduates with superior professional skills and career opportunities.</p>
<p>The students featured in this issue also deserve to be congratulated on their impressive achievements.&nbsp; Because Christine Kalil and Serguei Molokov represent very different ideas of what makes a model ENCS student, they show how diverse our student body is. Serguei has spent well over a decade within this Faculty and has become not only excellent student but also a well-liked instructor and invaluable staff member. Christine has just passed the halfway mark of her undergraduate career here at ENCS and is already blazing a bright trail, what with her dedicated involvement with ECA and her new presidency of the Concordia chapter of Women in Engineering.</p>
<p>As you'll see from our News, Events, and Accolades section, this Faculty has been upholding excellence across the board over the past few months. And as you'll see from our in-depth profile of Guy Gosselin, ENCS has in fact been doing just that for a quarter century and more!&nbsp; <br />I hope that you enjoy reading our stories in this Spring/Summer 2010 edition of the Faculty Quarterly. I'd like to wish one and all a great summer and look forward to writing to you again come the early fall.</p>
<p>With warm wishes for happy reading,</p>
<p><em>Robin A. L. Drew<br /></em>Dean, Faculty of Engineering and Computer Science</p>
<p>&nbsp;</p>
