<p><strong><em>Christine Kalil, BEng in Mechanical Engineering</em></strong></p>
<p><strong><img style="MARGIN: 0px 0px 20px 20px; FLOAT: right" class="mt-image-right" alt="Christine_0018.jpg" src="http://newsfeed.encs.concordia.ca/encs_test/Christine_0018.jpg" width="220" height="335" />What's your background? <br /></strong>I'm originally from Pointe-Claire, Quebec. I received my DEC in pure and applied sciences from <a class="zem_slink" title="John Abbott College" href="http://www.johnabbott.qc.ca/" rel="homepage">John Abbott College</a>.</p>
<p><strong>How did you first become interested in studying mechanical engineering?<br /></strong>I always wanted to know the real life applications to the things I was learning in math and physics. I also like the constant challenge of continuously learning new things and having the opportunity to work in a variety of different environments when I graduate, so mechanical engineering seemed like a great fit!</p>
<p><strong>Why did you choose Concordia?<br /></strong>I had a few friends that were in the engineering program at Concordia that convinced me that it was the right school for me. I liked that there was the opportunity to be part of the Co-op program, to gain industrial experience, to join the <a href="http://ciadi.concordia.ca/">Concordia Institute of Aerospace Design and Innovation</a>, or become part of the numerous engineering societies at Concordia. Another bonus was that small Concordia's class sizes give students the opportunity to get to know their classmates better and have more one-on-one time with their instructors.</p>
<p><strong>What are some of your favorite memories of studying at Concordia? <br /></strong>I would have to say that most of my favorite memories from Concordia have happened outside of the classroom such as participating in Engineering Games, the Quebec Engineering Competition, and the numerous events I have participated in through being part of the <a href="http://www.ecaconcordia.ca/">Engineering and Computer Science Students' Association</a>. I've also been lucky enough to experience classes with some of Concordia's best professors. For instance, I'll never forget my classes with Dr. Ramachandran, who came in on two Saturdays during the semester to offer a pre-midterm review and a pre-final review.</p>
<p><strong>How else are you involved in the Concordia community?<br /></strong>In January of 2009 I decided to become more involved within my University to improve my on-campus life. I took on the position of VP Sports and Competitions with the ECA. This position has allowed me to meet many of the great engineering students who study here at Concordia as well as the amazing faculty that support us in all our endeavors. As VP Sports and Competitions of the ECA, I organized sporting events for engineering students; oversaw our competitive teams such as SAE, <a href="http://www.encs.concordia.ca/news-and-events/faculty-quarterly/article/?a=2010/02/featured-event.php">Concrete Toboggan</a>, and Engineering Games; planned the <a href="../../entry.php_a_2009_10_a-look-back-at-eca-frosh-2009.php">2009 Frosh</a>; and managed the ECA's charitable organization, ECAid. One of my favorite accomplishments was organizing a $20,000 fundraiser from ECAid to the <a href="http://www.childrenfoundation.com/">Montreal Children's Hospital Foundation</a> last April. We sponsored a miracle hour during their annual Radiothon and volunteered to answer the pledge phones during that hour.&nbsp; </p>
<p>I'm now busily preparing for my next adventure in the world of Concordia engineering societies, as I will be taking over the role of President of Concordia chapter for <a href="http://wie.ecaconcordia.ca/">Women in Engineering</a> for the 2010-2011 year. I am hoping that in that role I can help to encourage other young women to consider a career in engineering and to encourage those already here at Concordia to involve themselves in as many extra curricular activities as they can.</p>
<p><strong>What are your plans after graduation? <br /></strong>After graduation I'd like to gain some experience in the field before returning to school to complete an MBA. I'm also hoping to travel a little bit.</p>
<p><strong>Where do you see yourself in 10 years?<br /></strong>Right now my future is a blank canvas with many possibilities! I'm particularly interested in renewable energy and sustainability and am hoping to pursue those subjects in my future career. I'd also love to someday start up my own consulting firm.</p>
<p align="right"><em>Photo by Marc Bourcier.</em></p>

