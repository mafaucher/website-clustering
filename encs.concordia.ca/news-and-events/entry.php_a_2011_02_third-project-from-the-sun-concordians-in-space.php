<font style="font-size: 0.8em;"><i>by: Russ Cooper &nbsp;|&nbsp; Source: Concordia Journal</i></font><br /><br /><p>There are many grandiose adjectives one can use to describe any given project. This time <i>stellar</i> would be 100% true. The student-run organization Space Concordia is preparing to build a
tiny satellite, fill it with one bright idea from some bright
Concordian, and put it into orbit. The team is building a
CubeSat - a 10 cm by 10 cm by 10 cm cube-shaped satellite with a total
weight limit of 1.33 kg. Now, they're looking for something to put in
it.</p><p align="center"><img alt="satellite.jpg" src="http://newsfeed.encs.concordia.ca/encs_test/satellite.jpg" class="mt-image-center" style="margin: 0pt auto 20px; text-align: center; display: block;" height="214" width="440" /><i><font style="font-size: 0.8em;">Space
Concordia members (left to right) Corey Clayton, Scott Gleason, Niccolo
Cymbalist, Nick Sweet and Zak Kain show a model of Space Concordia's
CubeSat project. To get into space, the CubeSat cannot propel itself.
Rather, it 'piggybacks' upon a larger satellite being launched. Once
there, the CubeSat releases from the satellite and proceeds to perform
its function. The what, where, and when of the larger satellite launch
for the Canadian Satellite Design Challenge has not yet been
determined. | Photo by Concordia University</font></i></p><br /><p> They've been requesting ideas from the Concordia community via their <a href="http://spaceconcordia.ca/">website</a>
with the following criteria: besides a few technical requirements, the
payload must have scientific value, it must be feasible and durable,
and it must be innovative. Also, it can't cost more than $10,000.<br /> <br />
"The whole challenge is to take up minimal space with a miniaturized
scientific instrument and do something useful," says Space Concordia
team leader and fourth-year mechanical engineering student Niccolo
Cymbalist. <br /> <br /> Thus far, they've had a handful of good ideas.One notable
suggestion was to equip the CubeSat with a small camera with a powerful
zoom, which would be controlled via a website by elementary school
students throughout Canada. "A teacher could say, 'Today we're going to
learn about space and science, and to support this activity, we'll put
the satellite overhead and take a picture,'" he says.<br /></p> <p> <br /> Another idea came from electrical and computer engineering (ECE) professor Nawwaf Kharma (see <i>Journal</i>, January 31, 2011), who suggested launching ants into space to observe how they build their nests in zero gravity.<br /> <br />
Whatever the winning idea, the team will enter their CubeSat into the
Canadian Satellite Design Challenge to compete with entries from 14
other Canadian universities. Each project entered will be tested for
functionality and durability in a simulated space environment. The best
design will be selected in October 2012 to launch into space at some
future date and used for scientific research.<br /> <br /> If they win, Concordia would become the first university in Quebec to launch a CubeSat.<br /> <br />
While the cosmic portion of the project seems quite impressive, the
team's interdisciplinary nature makes the initiative unique.<br /> <br />
Space Concordia comprises about 30 students, both grad and undergrad
from a wide range of departments, "all interested in stuff to do with
space," says second-year ECE student and VP Communication for Space
Concordia, Nick Sweet.<br /> <br /> Granted, most students are from
engineering, but the team also has a fine arts student who helps design
and maintain the website, multiple physics students, and they've been
in contact with some people in the natural science community to
determine needs for experiments that could potentially take on an
extraterrestrial dimension.<br /> <br /> "There is a kind of team learning going on. I really get boosted up in my education, which is quite helpful," says Sweet.<br /> <br />
"I see the mix of graduate and undergraduate students as a good thing,"
says ECE professor Scott Gleason, the group's faculty advisor. "Many
people say the CubeSat is just an undergraduate project, but in the
future, as instruments get smaller and smaller, they could be very
useful vehicles for graduate students to do real research," he says,
stating similar projects at his former workplaces, Stanford University
and the U.K.'s University of Surrey, were completed with "amazing
success." <br /> <br /> "In Montreal, there's a lot of thinking around
aerospace, but not much about astronautics," says Gleason. "This is
just the beginning of astronautics at Concordia. We want to build some
momentum with this project, get established, get some regular funding
so we can do some really interesting projects."<br /> <br /> What would you send into space? <a href="mailto:payload@spaceconcordia.ca">Email</a> Space Concordia.<br /> <br /> <b>Related links: <br /> </b>⬢&nbsp;&nbsp;&nbsp; <a href="http://spaceconcordia.ca/">Space Concordia</a><br /> ⬢&nbsp;&nbsp;&nbsp; <a href="http://www.facebook.com/groups/spaceconcordia">Space Concordia on Facebook</a><br /> ⬢&nbsp;&nbsp;&nbsp; <a href="http://www.twitter.com/spaceconcordia">Space Concordia on Twitter</a> <br /> ⬢&nbsp;&nbsp;&nbsp; Concordia <i>Journal</i> story on <a href="http://now.concordia.ca/what-we-do/research/20110131/this-class-is-unreal.php">Nawwaf Kharma, January 31, 2011</a><br /> ⬢&nbsp;&nbsp;&nbsp; <a href="http://www.geocentrix.ca/index.php?option=com_content&view=article&id=2&Itemid=4">Canadian Satellite Design Challenge</a><br /> ⬢&nbsp;&nbsp;&nbsp; <a href="http://thelinknewspaper.ca/article/903">Con U Wants to Send Your Ideas Into Orbit</a> - <i>The Link</i>, Jan. 25, 2011<br /> ⬢&nbsp;&nbsp;&nbsp; <a href="http://thelinknewspaper.ca/article/688">Concordia in Space</a> - <i>The Link</i>, Nov. 16, 2010</p><br />

