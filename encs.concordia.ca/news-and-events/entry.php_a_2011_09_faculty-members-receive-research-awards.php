			<div class="entry-meta" title="2011-09-14T15:44:49-05:00">Wednesday Sep 14, 2011</div>
		</div>
	
		<div class="entry-content">
			<div class="entry-body">
				
On Tuesday, September 13, Concordia's Office of the Vice President, Research and Graduate Studies held its annual Research Awards. Hosted by Louise Dandurand, the University's Vice President of Research and Graduate Studies, the evening reception honoured some Concordia's most promising and innovative researchers. <br /><br /><div align="center"><img alt="URA-2011.jpg" src="http://newsfeed.encs.concordia.ca/encs_test/URA-2011.jpg" class="mt-image-center" style="text-align: center; display: block; margin: 0pt auto 20px;" width="440" height="330" /><font style="font-size: 0.8em;"><i>Ted Stathopoulos, Robin Drew, and Zhigang Tian</i></font><br /></div><br />The Faculty of Engineering and Computer Science was front and centre at the ceremony. <a href="http://www.bcee.concordia.ca/index.php/Dr._T._Stathopoulos">Dr. Ted Stathopoulos</a> was presented with a Concordia University Research Award in the "established" subsection of the "Technology, Industry, and Environment" category. He was honoured alongside his colleague from the Concordia Institute of Information Systems Engineering, <a href="../resources/faculty-and-staff-directory/details/_id_Tian__20Z._d_09">Dr. Zhigang Tian</a>, who received a Petro Canada Young Innovator Award.<br /><br />The University has presented these awards in order to recognize their exceptional contributions, both within their respective fields and to the Concordia community as a whole. Through their continued success with substantial and sustained funding, their impressive records of publication and their dedication to graduate student supervision, they have emerged as our very best leaders and ambassadors. These awards are an acknowledgment of their accomplishments and an expression of thanks.<br /><br />The University Research Awards were first created in 1998, to recognize and promote excellence in research and creative activity at Concordia University.<br />

